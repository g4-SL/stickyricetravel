				<p>
					Often termed, "Sabah's Lost World", a reference to Sir Arthur Conan Doyle's mysterious land of immense, vine-clad jungles, majestic, towering, ancient trees, strange and rare wildlife - as well as a resident population of giant dinosaurs. The Maliau Basin Conservation Zone lives up to its nickname (albeit without the man-eating Tyrannosaurus) and is one of Sabah's least travelled gems. Situated in a remote Southern area of the state, Maliau Basin is almost completely encircled by insurmountable cliffs that have separated it from the rest of the world for centuries. Although some locals would have known of the area, no archaeological record has been found establishing any permanent residency ever in this zone. The first account of Maliau Basin by westerners was in the late 1940's when a small plane almost crashed into the cliff wall surrounding the area and it was not until 1988 that a modern expedition occurred revealing a virtually self contained ecosystem, glorious waterfalls, and an abundance of wildlife. 
				</p>	
				<p>
					Eventually protected by the government and designated as a Class 1 Protected Forest Reserve it remains one of the least explored, most pristine rainforests in all of Borneo. A trip to Maliau Basin is for the truly adventurous. Much of the region is still unexplored and completely wild. Having over 2,000 recorded types of different plants &amp; animals with others still waiting to be discovered, and including: lower montane forest, montane heath forest, lowland forest, and hill dipterocarp forest. The sheer amount of diversity found in the area is staggering. 
				</p>	
				<p>
					Reaching the Basin will necessitate the use of 4X4 vehicles. The jungle trekking will require an accompanying Forest Ranger as well as a guide and porters. A true jungle adventure, few people in the world have visited Maliau Basin. The accommodations &amp; meals are basic but if you yearn for a remote, memorable, and unique rainforest experience look no further - it is here.
				</p>	
				<p>
					The Maliau Basin Program begins and ends in Kota Kinabalu city. If this does not fit your travel plans, you can also start and end in Tawau. Note that a Maliau Basin trip can be classified as an expedition; expect remote jungle trekking of 5-8 hours daily and some lengthy section of these treks are uphill and strenuous. You have to be fit and must have a travel insurance policy that covers a helicopter evacuation. Satellite camps offer basic necessities in terms of sleeping, toilet/shower and kitchen facilities.
				</p>		
			
			
