							In the morning, proceed by longboat upriver to Wind Cave with its impressive "King's Chamber". Here, stalactites and stalagmites with delicate lace-like patterns are found. A short walk along a plankwalk next to the limestone cliff followed by a climb up approximately 250 concrete steps and you arrive at the entrance to Clearwater Cave. Like most caves in Mulu, Clearwater Cave provides a habitat for unique species of plants. One of these, the one-leafed plant can only be found at Mulu. Photo-sensitive algae grow on certain rocks and produce needle-like formations that point towards the source of light. Near Clearwater Cave there is a wonderful swimming area shaded by giant rainforest trees. Enjoy a simple picnic or packed lunch. After lunch proceed to Long Litut (1 hr boat ride). Start trekking to Camp 5 (3 hrs) through a mixed dipterocarp and riparian forest. Overnight at Camp 5 which offers basic dormitory accommodation with basic common toilet facilities.
