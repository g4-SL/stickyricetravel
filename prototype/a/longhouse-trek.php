			<p>
				Menyang Tais is 17-door Iban longhouse located a 20-30 minutes boat ride from the Batang Ai reservoir jetty. The 
				longhouse is home to approximately 80 people and is one of four longhouses at Batang Ai with a long-standing community 
				partnership with the operator. A trip to Menyang Tais offers the visitor a chance to learn about Iban culture and traditions. 
				Visitors sleep in a bilik or family 'apartment' of a host family in the longhouse. A range of activities such as farm visits, 
				jungle trekking and upriver excursions are available. The immediate vicinity of the longhouse is surrounded by farmland. The 
				main crop is hill rice and the villagers also plant various fruit and vegetables, along with commercial crops such as rubber 
				and pepper. Further away from the longhouse the farms merge into secondary and primary rainforest. This forest is part of the 
				planned Ulu Sungai Menyang conservation area and is home to a significant population of orangutans. Orangutan nests are often 
				sighted in the forest near Menyang Tais and occasionally orangutans are spotted in the canopy. 
			</p>
			
