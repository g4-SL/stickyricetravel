								<b>Arrive Mulu – Deer &amp; Lang Caves</b><br />
								Arrive Mulu airport in the morning and transfer to the park accommodation for check in. In the afternoon enjoy a scenic 3 km plankwalk through primary rainforest to the entrance of Deer Cave. Deer Cave is the world’s largest cave passage. Its scale its awesome and you are in no doubt how huge it is when you arrive at the cave entrance. The entrance of the cave is 150 m wide and 120 m high. Nowhere in this massive cave is the roof less than 90 m high. The cave has a moon-like landscape and from one vantage point, a perfect profile of President Lincoln can be observed. Next to Deer Cave is Langs Cave. The stalactites and stalagmites inside this cave are representative of the very best limestone formations in the park. After viewing the two caves, proceed to the Bat Observatory outside Deer Cave. Most evenings at sundown, a spectacular exodus of about 2 million of bats can be observed as they leave the cave in search of food. Then trek back to the park HQ by torchlight and return to accommodation.
