				<p>
				Discover the show caves of Mulu National Park, climb the Pinnacles and trek the headhunters' trail on a 5 day/4 night tour of 
				the Mulu UNESCO World Heritage Site. The Headhunter's Trail is a great way of entering or leaving Gunung Mulu National Park. 
				The trek combines upriver travel, jungle trekking and an overnight stay at an Iban longhouse. The trail itself follows the 
				route taken by Kayan headhunting parties who paddled up the Melinau River to Melinau Gorge. They then dragged their longboats 
				through the forest for 3 km until they reached the banks of the Terikan River, where they launched headhunting raids against 
				the people of the Limbang area. This trek offers an excellent introduction to the rivers and rainforest of Mulu, with the added 
				attraction of the Pinnacle climb and longhouse stay. 
