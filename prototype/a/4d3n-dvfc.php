				<p>If you are a wildlife and nature geek, you will find paradise in Danum Valley - a 472km sq conservation area of pristine, mostly primary, lowland dipterocarp forest that is home to 300 species of birds, mammals, insects etc. Sabah yesteryears' rampant jungle clearing left only a handful of forest untouched and Danum Valley Conservation Area is one of them. The area boasts an amazing diversity of animal life including the highly endangered Sumatran rhino, Borneo pygmy elephant, sun bear, orang utan and clouded leopard. Eight species of hornbills can be found here</p>
				<p>Lose yourself in the immense beauty of untouched nature as you watch the sun rises atop huge tree canopies. Your days will be spent walking and trekking in the forest in hope of seeing a mother orang utan with her baby, Wallace's flying frog, Borneon Gibbon, Rhinoceros Hornbill or the elusive Clouded Leopard. Trek to many waterfalls and visit Orang Sungai burial sites where ancient wooden carvings and coffin can be seen. Learn of forest regeneration and understand the complexity of the 130 million year old rainforest in one of the last places of its kind.</p>
				<p>The Danum Valley Conservation Area is about 2 and a half hours drive from the town of Lahad Datu that is accessible via public bus and plane. Yayasan Sabah manages the Danum Valley Conservation Area and there are only TWO accommodation options for nature visitors of the area. We recommend that you read in detail these options prior to booking with us. If you still have further questions regarding your visit to Danum Valley, please contact us.</p>
				<p>The Danum Valley Field Center was established in 1986 as a center for conducting researches and supporting scientists in rainforest studies. Today, the center is one of the leading field centers of its kind in Southeast Asia, fully equipped with laboratory, conference room, scientist and research assistants living quarters, dorms, camping grounds, eating lounge, and internet facility. </p>
				<p>As the name implies, it is first and foremost a research center designed for scientists. It is not a facility designed in mind to cater to tourist therefore there is minimal tourism hospitality and service. Over the years, the privilege of staying there has been extended to keen naturalists or independent travellers who want to be in an area that has a high concentration of wildlife and old forests. Visitors will be able to roam the area freely throughout the day - there is an abundant of wildlife within the Field Center itself. There are numerous trails that are marked and can be navigated on your own but it is also enjoyable to have a Ranger who can impart information about the forest. Danum Valley Field Center is recommended for keen naturalists and fuss-free, adventurous travelers. </p>

