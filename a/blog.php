				<h1>Horsfield's Tarsier (Cephalopachus bancanus)</h1>

				<p>
					This little mammal is one of our nocturnal residents that bounces from tree to tree. This tarsier has been placed in a separate genus from the tarsiers found in the Philippines and Sulawesi. They feed mainly on insects, but are also known to eat bats and reptiles. We usually find them at night, common in the Kinabatangan and in the Danum Valley, sitting on small trees looking for their next meal. These little primates also have the ability to rotate their heads over 180 degrees. We like tarsiers so much that we decided to make them our logo. Hopefully if you come to visit Borneo, you will have an opportunity to see Horsfield's Tarsier in the wild with us!
				</p>

