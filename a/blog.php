				<h1>Adventures in Murut Country, Sapulot</h1>

				<p>
					Over a long weekend we ventured into Murut Country in Sapulot, home to the Muruts who were once well-known for 
					their headhunting ways. The last of the tribes in Sabah to renounce headhunting, most of them today still 
					inhabit the jungles of Sabah in small pockets of communities, dotting inland riverbanks. Their thatched-roof 
					longhouses, woven bamboo baskets and singing hill myna conjure up romantic notions of traditional living, far 
					from the noise and hodgepodge of modern city life. The Muruts we visited welcomed us with hearty feasts, rice 
					wines and an extraordinary exciting adventure on their home grounds.
				</p>

				<p class="pt10">
					Modern civilization becomes more sparse as our car rolls past Keningau, and enters the frontier of Sabah. We pass villages 
					as the road winds through valleys and over hills to Sapulot. Trees abound wherever you look. We turned right into a meandering 
					dirt road, crossing bridges and streams, passing curious glances and smiley faces along the way. It finally brings us to the 
					Longhouse in Kampung Romol. On arrival, our lovely hosts prepared a Murut dinner feast of venison and jungle ferns. The full 
					moon made an appearance and out on the open lawn of the Longhouse, we sit contemplating life on full bellies.
				</p>

