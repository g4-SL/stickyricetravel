				<h1>Travel</h1>

				<p>
					Tonight at my group's introduction dinner I declared myself a Romantic. I recited 'Travel' by Edna Vincent Millay as a welcoming gesture, a symbol of the beginning of our travels through Vietnam, Laos and Cambodia, once known as Indochina.
				</p>

				<p class="pt10">
					When I was living in New York City I became obsessed with books and literature. In my revelation I found Blake and Millay. I admired their works and regarded both as rebels with causes - what I have always wanted to be. So some of Blake's words found their way onto my right calf to feed my tattoo addiction. Now living in Asia, my days browsing in bookstores are long gone; the book titles on the shelves seem mundane. Riddled with the realities of day to day life, I forget my fascination with words that once brought me plenty of joy and satisfaction.
				</p>

				<p class="pt10">
					And now, inspired by Joe whom I recently met during the first trip, I thought hard about poems I liked. This one struck a certain chord when I first developed an appetite for travel.
				</p>

				<p class="pt10">
					<b>'Travel' by Edna Vincent Millay</b><br /><br />
					The railroad track is miles away<br />
					And the day is loud with voices speaking<br />
					Yet there isn't a train goes by all day<br />
					But I hear its whistle shrieking
					<br /><br />
					All night there isn't a train goes by<br />
					Though the night is still for sleeping and dreaming<br />
					But I see its cinders red on the sky<br />
					And hear its engine steaming
					<br /><br />
					My heart is warm with friends I make<br />
					And better friends I'll not be knowing<br />
					Yet there isn't a train I wouldn't take<br />
					No matter where it's going
				</p>

