				<h2 class="grunge8" style="text-align:left">Padas White Water Rafting (Class III &amp; IV)</h2>
				<br />

				<p>
					A full day of adventure awaits when you decide to raft the Padas River! The Padas is located in a remote part of South Western Sabah which is currently best reached by train. After disembarking from the scenic railway ride at the Pangi station you will be served some light refreshment followed by a safety briefing conducted by experienced, professional rafting guides.
				</p>	
				<p>
					Within twenty minutes, your heart will be pounding as you shoot the rapids of the Padas River and experience some of the most exciting Class III &amp; IV white water rafting in all of South East Asia. You will traverse 7 thrilling rapids including The Cobra, The Lambada, and The Head Hunter and cover a distance of 9 kilometres over the course of 1.5 hours before finally ending at the Rayoh railway station to enjoy a delicious barbecue buffet lunch. Afterwards transfer back to Kota Kinabalu where you can share with others the story of how you conquered the mighty Padas river!
				</p>

