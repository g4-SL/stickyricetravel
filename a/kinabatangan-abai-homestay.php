					One of the most picturesque villages on the Kinabatangan, Abai village is located closest to the river mouth that pours itself out onto the Sulu Sea. Reachable only by a 2 hour boat ride from Sandakan pier (60km), the ride itself promises enchanting views of mangrove forests that line the coastal area before the river snakes its way onto the Kinabatangan Floodplain.
