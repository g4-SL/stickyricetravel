				<h2 class="grunge6">Responsible Traveler</h2>
				<div class="bgw pad">
					<p>
						The communities of Orang Sungai that inhabit the Kinabatangan Floodplain are conservative and Muslim. Please be sensitive to the local culture:
					</p>
					<p>
						Adhere to a modest dress code - for women, recommended shorts are around knee-length (or cover most thigh areas) and no low-cut or too tight fitting tops. It is highly recommended to cover shoulders. Men should never go without a shirt regardless of how humid you think the local weather is - this is considered rude and a sure-fire way of not being accepted in rural areas.
					</p>
					<p>
						Public display of affection is frowned upon and should be avoided at all times.
					</p>
					<p>
						Do not bring any alcohol into the village with you and never entertain any invitation by locals or tourist to consume alcohol within the village.
					</p>
					<p>
						Never give money to children or anyone in the village unless it is part of gratuities or tips. Instead, consider supporting in sustainable ways by supporting programs and projects by the community or NGOs that work with them.
					</p>
