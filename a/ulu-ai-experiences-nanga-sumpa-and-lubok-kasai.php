				<h2 class="grunge5">Program</h2>

				<table class="tbl_p">
					<tbody>
						<tr valign="middle">
							<th><img src="c/day1.png" alt="Day 1 - Day One" /></th>
							<td>
								Pick up from hotel at 8am in Kuching and drive to Batang Ai reservoir. Along the way stop at Lachau Bazaar. Stroll around the shops and the small native market and have an early lunch in a local coffee shop. Then continue your journey, arriving at Batang Ai Lake around 1.30 - 2.00pm. Board a longboat and travel upriver to Nanga Sumpa, arriving at approximately 3.30pm, although the exact time will depend on river conditions. Settle in at the lodge and have dinner. Overnight at Nanga Sumpa Lodge.
							</td>
						</tr>
						<tr>
							<th><img src="c/day2.png" alt="Day 2 - Day Two" /></th>
							<td>
								After breakfast, hike the jungle trails along the river. Then continue upriver by longboat. Our destination is the scenic Enseluai waterfall. Along the way (if conditions are suitable) your boatman may stop to demonstrate fishing Iban-style using a jala or throw net.  
								<br /><br />
								Visit Jambu longhouse, which is located approximately 30 minutes upriver from Nanga Sumpa Lodge. Jambu is a relatively small community with 8 families. It is the furthermost and most isolated community on the Delok River. After visiting Jambu continue your journey upriver. 
								<br /><br />
								Arrive at the waterfall and have time to relax, swim and enjoy the rainforest surroundings. Enjoy a riverside BBQ lunch prepared by your Iban hosts. 
								<br /><br />
								At about 1.30pm, proceed by boat for about 15 minutes to the junction of Delok and Lalang Rivers. Then, trek for 1 - 2 hours along a rainforest trail through orang utan habitat and arrive at Lubok Kasai Jungle Camp, located on the banks of the Lalang River. 
								<br /><br />
								Settle in and relax, and swim in the river or just enjoy the rainforest surroundings. Orangutans are occasionally seen in this area.  After dinner spend a quiet evening at the camp and enjoy the tranquility of the forest. Alternatively go on a half an hour night walk near the camp if weather permits. Overnight at Lubok Kasai Jungle Camp.
							</td>
						</tr>
						<tr>
							<th><img src="c/day3.png" alt="Day 3 - Day Three" /></th>
							<td>
								Breakfast at the camp. Then free at leisure to relax, swim in the river or take some photos around Lubok Kasai. At about 10am, travel downriver back to Nanga Sumpa, enjoy early lunch at about 11am and depart around noon to the Batang Ai Lake jetty, arriving at approximately 1.30pm. Then travel overland back to Kuching. Arrive in Kuching at about 6.30pm and be dropped off at your hotel.
								<br /><br />
								<i>Weather conditions and water level of local rivers may force us to modify the tour in the interests of safety. Itinerary above is for guideline only.</i>
							</td>
