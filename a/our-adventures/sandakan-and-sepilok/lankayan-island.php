					<p>
						Lankayan is a small tropical island in the Sulu Sea, about 90 minutes by boat from the port of Sandakan, and sits within a large Marine Protected Area with excellent diving. Unpopulated and circled by white sandy shore, Lankayan Island is also exquisitely private, perfect for honeymooners and anyone seeking peace and tranquility. 
					</p>				
					<p>
						The only resort on the island, Lankayan Island Dive Resort, was built in 1997 with a lot of sustainability thoughts and is an outstanding example of how eco-tourism can contribute to conservation. Today it boast a privately-managed conservation area ‘SIMCA’ (Sugud Islands Marine Conservation Area), an area of 467 km square including 3 main atolls consisting of Lankayan, Tegaipil and Billean. These 3 islands are historic nesting places for the endangered Green and Hawksbill turtles, and SIMCA’s roles via their establised Reef Guardian enforcement is to patrol establised no-take zones, protect turtle eggs and monitor the long-term environmental change in coral cover, fish life, water quality and sedimentation.
					</p>	
					<p>
						So if you want your tourism-dollars to go a little bit further – a percentage of what you pay go into its conservation fund to sustain the patrolling of boats by locally trained wardens, turtle hatchery, and other marine conservation efforts activities – Lankayan Island Dive Resort is the place to go.
					</p>			
					<p>
						Conservation efforts aside, the resort is lovely, consisting of 23 wooden, airy chalets right on the beach so you wake up to turquoise, clear water and sea breeze. Every chalet also has its own private balcony and direct access to the beach. Delicious fresh meals and snacks are available throughout the day in the central building with its lovely terrace. The emphasis is on locally caught seafood, with barbecues held at the bar.
					</p>	
					<p>
						If you are an avid diver, diving here is wonderful, with a centre run by professional dive-professionals; 3 boat dives per day is included, or you can simply buddy up and dive the excellent house reef straight from the shore. Non-divers can keep busy with snorkeling, kayaking or visiting the turtle hatchery.
					</p>


