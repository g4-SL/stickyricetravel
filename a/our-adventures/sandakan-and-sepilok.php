				<h1>Sandakan &amp; Sepilok</h1>
				<p>
				Very few visitors to the Malaysian Borneo go without including a visit to Sepilok, home to the rehabilitation center for the orang utans, the Sepilok Orangutan Rehabilitation Center (SORC).  A visit to Sepilok is like visiting a big rainforest park teeming with flora and fauna. You can spend your day hopping between the Sepilok Orangutan Rehabilitation Center (SORC), Bornean Sun Bear Conservation Centre (BSBCC) and Rainforest Discovery Center (RDC), with your senses and mind tickled by seeing wildlife and learning of the conservation efforts here. 
				</p>
				<p>
				The Sepilok Orangutan Rehabilitation Center (SORC) was founded in 1964 to rehabilitate orphaned orangutans. The site consists of 43 sq kilometres of protected land at the edge of the Sepilok-Kabili Forest Reserve, and facilities include a centre built to care for the rescued orang utans. Sepilok is run by the Sabah Wildlife Department and is considered to be a useful educational tool for creating awareness of the plight of orang utans, for both locals and visitors alike.
				</p>	
				<p>
				The orang utans may be the famed attraction in Sepilok but in reality, the newly-opened Bornean Sun Bear Conservation Centre (BSBCC) is just as impressive. The aim of the center is to rehabilitate Bornean sun bears, the smallest, most arboreal and least studied bear. These bears are also mainly diurnal and do not hibernate but build nests in trees to sleep in.
				</p>	
				<p>
				Not to be missed in the Sepilok-Kabili Forest Reserve is the Rainforest Discovery Center (RDC). Set up originally as an environmental education center for students and teachers, it opened its doors to visitors in 2007 and today is one of the main attractions of any visits to Sepilok because of its excellent set up by Sabah Forestry Department. Discover the rainforest of Borneo from the treetops, along a 347 metre-long steel, canopy walkway and explore the extensive network of rainforest discovery trails on its grounds.
				</p>		
				<p>
				Half an hour from Sepilok, Sandakan is the second biggest city in Sabah after Kota Kinabalu. Once the richest city in the area, it was once the main port for oil, tobacco, coffee, sago and timber exports. Today, other economic activities include fishing, ship building, and tourism. A day in Sandakan warrants a visit to the Sandakan Memorial Park, St Michael &amp; All Angelsâ€™ Church, Agnes Keith House and English Tea House &amp; Restaurant, and Buli Sim Sim traditional stilt water village to name a few. 
				</p>
