								<th><img src="c/day1.png" alt="Day 1 - Day One" /></th>
								<td>
									Arrive at Mulu airport in the morning and transfer to the park accommodation for check in. In the afternoon enjoy a scenic 3 km plankwalk through primary rainforest to the entrance of Deer Cave, the world’s largest cave passage. The entrance of the cave is 150 metre wide and 120 metre high - nowhere in this massive cave is the roof less than 90 metre high. The cave has a moon-like landscape and from one vantage point, you can see a perfect profile of President Lincoln. Next to Deer Cave is Langs Cave. The stalactites and stalagmites inside this cave are representative of the very best limestone formations in the park. After viewing the two caves, proceed to the Bat Observatory outside Deer Cave. A spectacular exodus of about 2 million of bats can be observed at sundown as they leave the cave in search of food. Then trek back to the park HQ by torchlight and return to your accommodation.
