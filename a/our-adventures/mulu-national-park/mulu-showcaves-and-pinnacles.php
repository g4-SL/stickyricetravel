								<td>
									In the morning, proceed by longboat upriver to Wind Cave with its impressive ‘King’s Chamber’. Here, stalactites and stalagmites with delicate lace-like patterns are found. Walk along a plankwalk next to the limestone cliff and climb up approximately 250 concrete steps to reach the entrance of Clearwater Cave. Like most caves in Mulu, Clearwater Cave provides a habitat for unique species of plants such as the one-leafed plant which can only be found at Mulu. Photo-sensitive algae grows on certain rocks and produce needle-like formations that point towards the source of light. 
									<br /><br />
									Nearby Clearwater Cave, there is a wonderful swimming area shaded by giant rainforest trees. Enjoy a simple picnic or packed lunch. After lunch, proceed to Long Litut which is an hour boat ride away. Start trekking to Camp 5 for approximately 3 hours through a mixed dipterocarp and riparian forest. Overnight at Camp 5.
