					The Kinabatangan Floodplain is an exceptional area of Sabah, an area recognized for its natural beauty and role in supporting diverse life. 
				</p>
				<p>
					Consisting of five distinct habitats - waterlogged and dry forests, saline and freshwater swamps and limestone habitats - the lower Kinabatangan has one of the most diverse concentrations of wildlife in Borneo. Expect to see Proboscis monkeys, hornbills, crocodiles, kingfishers, langurs, gibbons and various species of birds. Pygmy elephant and orang utans in the wild are also common. A visit to the river offers dusk and dawn boat cruises along the river, light trekking and night walks, and usually a visit to forest restoration areas managed by local communities, tour operators or conservation agencies.
				</p>	
				<p>	
					We support a myriad of operators on different parts of the river including commercial operators and community tourism initiatives by local communities. Borneo Nature Lodge in Sukau and Kinabatangan Wetlands Resort in Abai are both commercial lodges with sensitive tourism practices, while the homestays and rustic camps run entirely by the communities of Abai and Batu Puteh villages are great off-the-beaten-track places to go on a river adventure with an added element of culture. 
