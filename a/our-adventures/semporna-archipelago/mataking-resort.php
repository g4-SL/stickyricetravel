				<p>
					A trip to the Reef Dive Resort on Mataking Island, a private island on the Celebes Sea, is like a dream come true. While a lot of the other resorts in the archipelago caters mostly to the diver scene, at Reef Dive Resort you wiIl find that diver or not, there is a place for you here. Think beautiful villas on the beach, mesmerizing aquamarine waters and white powder sand beaches - if you are going to splurge on an island trip in Sabah, Reef Dive Resort is the place to do it.
				</p>	
				<p>
					About an hour speedboat ride from Semporna, Reef Dive Resort is the only resort located on Mataking Island. The island is within close proximity to the Tun Sakaran Marine Park comprising of the islands of Bodgaya, Boheydulang, Sebangkat, Selakan, Maiga, Sibuan, Mantabuan, and the patch reefs of Church and Kapikan. Mataking is made up of 2 islands - the small Mataking Island is connected to the main island during low tide via a short sandy stretch.
				</p>	
				<p>
					Climb the wooden observation tower for a breathtaking view of Mataking and the surrounding seascape, visit the Turtle Hatchery, participate in batik painting, go jungle trekking, explore the surrounding Marine Park and meet the local community of Sea Gypsies 'Bajau Laut', and mail a postcard from Mataking's underwater mailbox. Divers can gear up and dive the surrounding reefs, and arrange for Sipadan dives depending at permit availability. The dive center at the resort is of 5 star PADI rating. At the end of the day, pamper yourself to a massage at the Jamu Spa.
				</p>		

