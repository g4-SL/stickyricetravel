							<td>
								Pick up from hotel at 8am in Kuching and drive to Batang Ai reservoir. Along the way stop at Lachau Bazaar. Stroll around the shops and the small native market and have an early lunch in a local coffee shop. Then continue your journey, arriving at Batang Ai Lake around 1.30 - 2.00pm. Board a longboat and travel upriver to Menyaing Tais, arriving at approximately 3.30pm, although the exact time will depend on river conditions. Then, take a 15 minutes walk to the longhouse. 
								<br /><br />
								Upon arrival, meet your Iban hosts. In the evening, relax on the ruai (common verandah) and spend time with the Iban people. Learn of their culture and enjoy some local rice wine after dinner. Overnight in Menyaing Tais Longhouse.
