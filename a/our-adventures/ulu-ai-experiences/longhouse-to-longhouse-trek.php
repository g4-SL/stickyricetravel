				<p>
					This trek gives you the opportunity to see not only one Iban community but two; both settlement located on the edge of the forest in beautiful settings,  and offer a great opportunity to experience and understand Iban life. 
				</p>
				<p>
					Menyang Tais is a 17-doors Iban longhouse, a 20 - 30 minutes boat ride from the Batang Ai reservoir jetty. The longhouse is home to approximately 80 people and is one of the four longhouses at Batang Ai with a long-standing community partnership with the operator. 
				</p>
				<p>
					A trip to Menyang Tais Longhouse offers visitor the chance to learn about Iban culture and traditions. Visitors sleep in a bilik or family ‘apartment’ of a host family in the longhouse. A range of activities such as farm visits, jungle trekking and upriver excursions are available. The immediate vicinity of the longhouse is surrounded by farmland. The main crop is hill rice and the villagers also plant various fruits and vegetables, along with commercial crops such as rubber and pepper. Further away from the longhouse, the farms merge into secondary and primary rainforest. This forest is part of the planned Ulu Sungai Menyang conservation area and is home to a significant population of orangutans. Orangutan nests are often sighted in the forest near Menyang Tais and occasionally spotted in the canopy.
				</p>
				<p>
					Nanga Sumpa in Ulu Ai, on the other hand, is one of the best area for a longhouse experience. What makes Ulu Ai so special is the natural environment and stunning scenery - clean rivers and jungle streams, refreshing waterfalls and undisturbed tracts of rainforest with plenty of culture and outdoor activities.
				</p>

