						<tr>
							<th><img src="c/day2.png" alt="Day 2 - Day Two" /></th>
							<td>
								After breakfast, begin a ‘Longhouse to Longhouse’ jungle trek in the high conservation value forest located between the Menyang River and the Delok River.
								<br /><br />
								You may see orangutan nests along the way or if you are lucky, spot a wild orangutan. Hornbills, argus pheasants, barking deer, long and pig-tailed macaque monkeys and wild boar also live in the area. Trek for 4 - 5 hours and then set up camp in the middle of the Borneo rainforest. Help your Iban guides gather jungle ferns, bamboo shoots or other edible plants that may be available in the area. Before darks falls, take your evening ‘bath’ in a jungle stream. Relax before having dinner. Overnight in Sibau Jungle Camp.
