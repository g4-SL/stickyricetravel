							<td>
								Pick up from hotel at 8am in Kuching and drive to Batang Ai reservoir. Along the way stop at Lachau Bazaar. Stroll around the shops and the small native market and have an early lunch in a local coffee shop. Then continue your journey, arriving at Batang Ai Lake around 1.30 - 2.00pm. Board a longboat and travel upriver to Nanga Sumpa, arriving at approximately 3.30pm, although the exact time will depend on river conditions. Settle in at the lodge and have dinner. Evening free at leisure. Overnight at Nangan Sumpa Lodge.
