							<td>
								After breakfast, hike the jungle trails along the river. Then continue upriver by longboat. Our destination is the scenic Enseluai waterfall. Along the way (if conditions are suitable) your boatman may stop to demonstrate fishing Iban-style using a jala or throw net. 
								<br /><br />
								Visit Jambu Longhouse, which is located approximately 30 minutes upriver from Nanga Sumpa Lodge. Jambu is a relatively small community with 8 families. It is the furthermost and most isolated community on the Delok River. After visiting Jambu, continue your journey upriver. 
								<br /><br />
								Arrive at the waterfall and have time to relax, swim and enjoy the rainforest surroundings. Enjoy a riverside BBQ lunch prepared by your Iban hosts. 
								<br /><br />
								At about 1.30pm, proceed by boat for about 15 minutes to the junction of Delok and Lalang Rivers. Then, trek for 1 - 2 hours along a rainforest trail through orangutan habitat and arrive at Lubok Kasai Jungle Camp, located on the banks of the Lalang River.
								<br /><br />
								Settle in and relax, and swim in the river or just enjoy the rainforest surroundings. Orangutans are occasionally seen in this area.  After dinner spend a quiet evening at the camp and enjoy the tranquility of the forest. Alternatively go on a half an hour night walk near the camp if weather permits. Overnight at Lubok Kasai Jungle Camp.
							</td>
						</tr>
						<tr>
							<th><img src="c/day3.png" alt="Day 3 - Day Three" /></th>
							<td>
								After breakfast, depart at 8.30am and go trekking on one of the rainforest trails to look for orangutans and other wildlife like pig-tailed macaques, hornbills and a variety of birds. While orangutans are only occasionally spotted, their nests are often seen along the trails. Return to Lubok Kasai between 12 - 1pm. Have lunch at the camp. Rest of the afternoon is free at leisure. Go for a swim in the river or relax in the rainforest surroundings.  At 4pm, depart on a 1 - 2 hour walk along a trail that follows the Lalang River in search of orangutans. Enjoy dinner before lights out. Overnight at Lubok Kasai Jungle Camp.
