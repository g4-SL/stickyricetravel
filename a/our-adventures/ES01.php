									<br /><br />
									After lunch, embark on a village sightseeing walk. Get a feel of what life is like for locals by wandering around the village into fruit orchards, school, tree nurseries, or paddy plantation. Try your hands at mending fish nets or traps, craft-making or paddy planting.
