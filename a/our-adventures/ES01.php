									Upon your return to the village, have lunch and germinate seedlings in tree nurseries before relaxing for the afternoon. After tea time, continue to relax on your homestay porch or go fishing and learn traditional prawn trapping. Alternatively, you can also play soccer with the village kids, or watch the sun sets over the river. After dinner, go on a night river safari, spotting fireflies and nocturnal wildlife along the way.
