				<p>
					Located on the banks of the Delok River, the Nanga Sumpa Lodge is approximately a 4.5 hour land transfer from Kuching, followed by a further 1.5 hour longboat ride from the Batang Ai Lake jetty. It is considered a relatively remote area of Borneo, and its facilities reflect the remote location and are basic yet comfortable. This award-winning operation was established in 1987 in partnership between the people of Nanga Sumpa and a local tour operator.
				</p>
				<p>
					Nanga Sumpa Lodge serves as a base for a range of nature-based activities and excursions, and offers visitors the chance to learn about the culture and lifestyle of the Iban community who live in the nearby longhouse. 
				</p>
				<p>
					Activities at Nanga Sumpa include travelling further upriver by longboat to have a swim at a waterfall; visiting the longhouse and surrounding farms; participating in the daily activities of the Iban people; and jungle trekking on trails that take you along streams and through farmlands to primary rainforest.  
				</p>

