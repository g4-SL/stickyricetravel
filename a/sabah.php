				<h4>
					For those seeking for thrilling adventures, relaxing ambience and an unforgettable cultural experience, look no further than Sabah, the Land Below the Wind! It is often known as the home to Kinabalu National Park, the first UNESCO World Heritage Site of Malaysia. Its cooling fresh air is always a treat for both local and international travellers to combat the humid Malaysian weather. The adventerous can also attempt the climb up Mount Kinabalu with its summit at the height of 4,096 metres. Danum Valley and Maliau Basin is another site that is often travelled to when at Sabah. Coined as Sabah's Lost World, Maliau Basin is a mysterious land of immense, vine-clad jungles, majestic, towering ancient trees and rare wildlife while Danum Valley takes pride in being a 130-million-year old Borneo virgin rainforest filled with its own strange and rare wildlife. 
					<br /><br />
					Sabah is not all about its mountainous terrains and magnificient rainforest. The coast of Sabah is filled with beautiful islands that could compete with the likes of Maldives. Here, one can dive the surrounding reefs or opt for a more relaxing past time on the white sandy beach. Not forgetting the rich culture in Sabah, travellers can have first hand experiences on the culture of the indigenous people. Aggressive, yet warm and welcoming, they are always open to new guests and would love to talk about their history and way of life. 
