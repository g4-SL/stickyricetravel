					Mount Kinabalu is the heart and soul of Sabah - ultimate pride of its people. The highest mountain peak between the Himalayas and New Guinea, the mountain holds a distinctive position in terms of biodiversity of Southeast Asia. A UNESCO World Heritage Site, the area surrounding the mountain offers a wide array of habitats from rich tropical lowland and hill rainforest to tropical montane forest, sub-alpine forest and scrub on its highest elevations. Ultramafic forests cover about 16% of the park. It has also been identified as a Centre of Plant Diversity for Southeast Asia as it contains representatives from at least half of all Borneo’s plant species. In terms of Borneo’s birds, mammals and amphibian species, including reptiles, the area is considered an important centre of endemism.
