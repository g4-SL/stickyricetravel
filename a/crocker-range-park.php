				<p>
					The Crocker Range Park was designated to be a forest reserve in 1968, followed by further efforts established in 1984 to protect the water catchment areas that supply drinking water to the west coast and interior of Sabah. Today, it is managed by Sabah Parks with rising concern to protect its rich biodiversity and rare species of flora and fauna inhabiting these forest areas. The park area and boundaries are home to numerous settlements of Kadazandusuns and Murut communities still living traditionally, practicing subsistence shifting agriculture and hunting.
				</p>
				<p>
					The park receives few visitors, mainly because the spectacular scenery of  Kinabalu Park area takes precedence, but also due to limited exposure and shortage of tourism facilities.
				</p>	
				<p>	
					The main attraction of Crocker Range Park lies in the ecosystem found in the area; five vegetation types of forests consisting of montane forest, lower montane forest, upper montane dipterocarp forest, hill dipterocarp forest and lower land forest. Rich with flora and fauna, a visit here offers the chance to see tropical vegetation and wildlife not found anywhere else but in Borneo. 
				</p>
				<p>
					Choose from a range of overnight programs including the Salt Trail expedition, Gunung Alab hike, Trusmadi Expedition and traditional Dusun village stay, or simply go for a daytrip to visit the Rafflesia Information Center, Kipandi Butterfly Park and finish off with a dip at Mahua Waterfall. 
