				<p>
					Often termed, "Sabah's Lost World", a reference to Sir Arthur Conan Doyle's mysterious land of immense, vine-clad jungles, majestic, towering, ancient trees, strange and rare wildlife - as well as a resident population of giant dinosaurs. The Maliau Basin Conservation Zone lives up to its nickname (albeit without the man-eating Tyrannosaurus) and is one of Sabah's least-travelled gems. Situated in a remote Southern area of the state, Maliau Basin is almost completely encircled by insurmountable cliffs that have separated it from the rest of the world for centuries. Although some locals would have known of the area, no archaeological record has been found establishing any permanent residency ever in this zone. The first account of Maliau Basin by westerners was in the late 1940's when a small plane almost crashed into the cliff wall surrounding the area and it was not until 1988 that a modern expedition occurred revealing a virtually self contained ecosystem, glorious waterfalls, and an abundance of wildlife.
				</p>	
				<p>
					Eventually protected by the government and designated as a Class 1 Protected Forest Reserve, it remains one of the less-explored, most pristine rainforests in all of Borneo. A trip to Maliau Basin is for the truly adventurous. Much of the region is still unexplored and completely wild. Having over 2,000 recorded types of different plants &amp; animals with others still waiting to be discovered. The area also boasts lower montane forest, montane heath forest, lowland forest, and hill dipterocarp forest, and the sheer amount of diversity found in the area is staggering. A lot of travelers make the assumption that because the area is remote and the foret is old-growth, they will see a lot of wildlife. This is not true on the basis that you will be hiking deep into the heart of Maliau where the jungles are thick, unexplored and less penetrable, making wildlife spotting and observations more challenging. During your first night at the center, we include a night drive and this will be the best opportunity to spot nocturnal mammals, during the rest of your hiking days the focus will be on flora and other creatures such as reptiles, insects and amphibians.
				</p>	
				<p>
					Reaching the Basin will necessitate the use of 4X4 vehicles and the expedition will require an accompanying forest ranger, as well as our nature guide and porters. A trip here will be a true jungle adventure with very low visitor numbers throughout the year. The accommodations &amp; meals are basic but if you yearn for a remote, challenging and unique rainforest experience, look no further - it is here. 
				</p>	
				<p id="sto">
					A Maliau Basin expedition is for the truly geeky at heart when it comes to nature, and you also have to be prepared to walk 5-8 hours daily with some extended, strenuous uphill sections throughout your time there. You have to be fit and must have a travel insurance policy that covers a helicopter evacuation. The Maliau Basin Program begins and ends in Kota Kinabalu city. If this does not fit your travel plans, you can also start and end in Tawau.
				</p>	

