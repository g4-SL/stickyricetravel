	    maxZoom: 18,
	    id: 'slouiseleong.cifdla6ah5l8xsem7cwbuiafc',
	    accessToken: 'pk.eyJ1Ijoic2xvdWlzZWxlb25nIiwiYSI6ImNpZmRsYTdyeDRhOG5yN2x4dDJrbWJmbW8ifQ.2zXPe7c1UZ491DMDODe65Q'
	}).addTo(map);
    
    $.when(getJson()).done(function(data){

    	function getColor(d) {
		    return d == "Sarawak" 	? '#E31A1C' :
		           d == "Sabah" 	? '#FC4E2A' :
		                      		  '#FFEDA0';
		}

		function style(feature) {
		    return {
		        fillColor: getColor(feature.properties.title),
		        weight: 2,
		        opacity: 1,
		        color: 'white',
		        dashArray: '3',
		        fillOpacity: 0.7
		    };
		}

		function highlightFeature(e) {
			if (e.target.feature.geometry.type == "Polygon"){
			    var layer = e.target;

			    layer.setStyle({
			        weight: 2,
			        color: '#666',
			        dashArray: '',
			        fillOpacity: 0.7
			    });

			    if (!L.Browser.ie && !L.Browser.opera) {
			        layer.bringToFront();
			    }
			}
		}

		function resetHighlight(e) {
			if (e.target.feature.geometry.type == "Polygon"){
		    	markers.resetStyle(e.target);
		    }
		}

		function clickEvent(e) {
			if (e.target.feature.geometry.type == "Polygon" && e.target.feature.properties.title == "Sarawak"){
				$('html, body').animate({
				    scrollTop: ($('#sarawak').offset().top)
				},500);
			}
			else if (e.target.feature.geometry.type == "Polygon" && e.target.feature.properties.title == "Sabah"){
				$('html, body').animate({
				    scrollTop: ($('#sabah').offset().top)
				},500);
			}
			else if (e.target.feature.geometry.type == "Point"){
				map.setView([e.latlng.lat+3, e.latlng.lng], 7);
			}
		}

		function onEachFeature(feature, layer) {
		    layer.on({
        		mouseover: highlightFeature,
        		mouseout: resetHighlight,
		        click: clickEvent
		    });

		    if (feature.properties && feature.properties.description && feature.properties.title && feature.properties.url && feature.properties.image) {
		    	var content = '<div class="popup-text-container"><div style="text-align:center"><img src="' + feature.properties.image + '" style="width:90%"></div><h2 class="popular_subtitle">' 
		    		+ feature.properties.title + '</h2><p>' 
		        	+ feature.properties.description + '</p><div style="text-align:center"><a href="'
		        	+ feature.properties.url + '">Read More</a></div></div>';
		        layer.bindPopup(content);
		    }
		}
		
		function pointToLayer(feature, latlng) {
		   var smallIcon = L.icon({
              iconSize: [64, 64],
              iconAnchor: [29, 64],
              popupAnchor:  [0, -64],
              iconUrl: './c/map-pin.png'
		   });

		   return L.marker(latlng, {icon: smallIcon});
		}

		var markers = L.geoJson(geojsonFeature, {
			style: style,
			pointToLayer: pointToLayer,
		    onEachFeature: onEachFeature
		}).addTo(map);
	});

	function getJson(){	
		return $.ajax({
			dataType: "json",
			url: "../f/map.json",
			success: function(data){
				geojsonFeature = data;
			}
		});
	}

});
