				<p>
					Located in the Sulu sea, Libaran Island is home to about 450 inhabitants whose main livelihood is derived from fishing and boat making. A visit here will give you a chance to observe Pacific Green or Hawksbill turtles coming ashore to build nests and lay their eggs. Walai Penyu Resort has an in-house turtle hatchery via its Turtle Conservation Programme, and collects and rescue eggs to hatch safely from predators before being released into the sea. The programme also aims to create awareness on endangered turtles for locals and visitors alike, and you may have the chance to assist in releasing baby turtles into the sea during your time here.
